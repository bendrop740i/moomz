const TOPICS = ["food", "couple", "drama", "tech", "lifestyle", "debat", "sport", "musique", "money", "ado"];

const families = [
  ...CORE,
  ...TOPICS.map((t) => ({ id: `tvote_${t}`, metric: `topic:${t}`, tiers: [5, 20, 60, 150, 400, 1000] })),
  ...TOPICS.map((t) => ({ id: `tcreate_${t}`, metric: `tpoll:${t}`, tiers: [1, 3, 10, 25, 60, 150] })),
];

function coinRewardFor(threshold) {
  const raw = 3 + 4 * Math.pow(Math.log10(threshold + 1), 1.6);
  return Math.max(3, Math.min(250, Math.round(raw)));
}

const rows = [];
for (const fam of families) {
  for (const threshold of fam.tiers) {
    rows.push(
      `  ('${fam.id}.${threshold}', '${fam.id}', '${fam.metric}', ${threshold}, ${coinRewardFor(threshold)})`,
    );
  }
}

const famIds = families.map((f) => `'${f.id}'`).join(", ");

const sql = `-- 016b: Achievement catalog seed — GENERATED by scripts/gen-achievement-defs.mjs.
-- ${rows.length} achievements across ${families.length} families. Idempotent.

-- Prune defs whose family no longer exists (e.g. renamed families).
delete from public.achievement_defs where family not in (${famIds});

insert into public.achievement_defs (id, family, metric, threshold, coin_reward) values
${rows.join(",\n")}
on conflict (id) do update set
  family = excluded.family,
  metric = excluded.metric,
  threshold = excluded.threshold,
  coin_reward = excluded.coin_reward;
`;

const out = path.join(process.cwd(), "supabase-migrations", "016b-achievement-defs-seed.sql");
fs.writeFileSync(out, sql, "utf8");
console.log(`Wrote ${out} — ${rows.length} achievement rows.`);
